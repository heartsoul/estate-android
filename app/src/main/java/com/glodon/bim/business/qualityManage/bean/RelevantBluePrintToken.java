public class RelevantBluePrintToken {
    public String code;
    public String message;
    public String data;

    @Override
    public String toString() {
        return "RelevantBluePrintToken{" +
                "facilityCode='" + code + '\'' +
                ", message='" + message + '\'' +
                ", data='" + data + '\'' +
                '}';
    }
}
